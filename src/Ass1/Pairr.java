package Ass1;

public class Pairr {
	int weight;
	int value;
	public Pairr(int weight2, int value2) {
		weight = weight2;
		value = value2;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
}
